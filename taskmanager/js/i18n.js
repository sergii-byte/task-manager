    t(key) {
        return this.translations[this._lang]?.[key] || this.translations.en[key] || key;
    },

    setLang(newLang) {
        this.lang = newLang;
    },

    toggle() {
        this.lang = this._lang === 'en' ? 'uk' : 'en';
        App.applyI18n();
        App.refresh();
    },
